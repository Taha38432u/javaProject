    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String userName, password;
        userName = txtusername.getText();
        password = txtpassword.getText();
        String dataUserName = "";
        String dataPassword = ""; // userName and passwordAdmin
        try {
            Connection con = ConnectionClass.db();
            String query = "SELECT * FROM passwordLogin WHERE id = ?";
            PreparedStatement ps = con.prepareStatement(query);
            ps.setInt(1, 1);

            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                dataUserName = rs.getString("userName");
                dataPassword = rs.getString("passwordAdmin");
            }

            rs.close();
            ps.close();
            con.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
        if (userName.equals(dataUserName) && password.equals(dataPassword)) {
            JOptionPane.showMessageDialog(null, "Login is successfull");
            Dashboard obj = new Dashboard();
            obj.show();
        } else {
            JOptionPane.showMessageDialog(null, "Login is not  successfull");
        }
    }//GEN-LAST:event_loginActionPerformed

    private void forgetPasswordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_forgetPasswordMouseClicked
        JFrame frame = new JFrame();
        frame.setLayout(new FlowLayout());
        frame.setSize(new Dimension(250, 150)); // Adjusted size for better display
        frame.setLocationRelativeTo(null); // Center the frame on the screen

        JLabel label = new JLabel("Enter your password:");
        JTextField password = new JTextField(15);

        JButton button = new JButton("Submit");
        button.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String enteredPassword = password.getText();
                try {
                    Connection con = ConnectionClass.db();
                    String query = "SELECT * FROM passwordLogin WHERE id = ?";
                    PreparedStatement ps = con.prepareStatement(query);
                    ps.setInt(1, 1);

                    ResultSet rs = ps.executeQuery();
                    if (rs.next()) {
                        String defaultPassword = rs.getString("defaultPassword");
                        if (defaultPassword.equals(enteredPassword)) {
                            // Prompt user for new username and password
                            String newUsername = JOptionPane.showInputDialog(frame, "Enter new username:");
                            String newPassword = JOptionPane.showInputDialog(frame, "Enter new password:");

                            // Update passwordLogin table with new username and password
                            query = "UPDATE passwordLogin SET username = ?, passwordAdmin = ? WHERE id = ?";
                            ps = con.prepareStatement(query);
                            ps.setString(1, newUsername);
                            ps.setString(2, newPassword);
                            ps.setInt(3, 1);
                            int rowsAffected = ps.executeUpdate();

                            if (rowsAffected > 0) {
                                JOptionPane.showMessageDialog(frame, "Password updated successfully!");
                            } else {
                                JOptionPane.showMessageDialog(frame, "Failed to update password!");
                            }
                        } else {
                            JOptionPane.showMessageDialog(frame, "Incorrect password!");
                        }
                    }
                    rs.close();
                    ps.close();
                    con.close();
                } catch (Exception e2) {
                    e2.printStackTrace();
                }
            }
        });

        frame.add(label);
        frame.add(password);
        frame.add(button);

        frame.setVisible(true);


    }//GEN-LAST:event_forgetPasswordMouseClicked

